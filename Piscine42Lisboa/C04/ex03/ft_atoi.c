#include <stdio.h>

int	ft_atoi(char *str)
{
	int	i;
	int	sign;
	int	nb;

	i = 0;
	while (str[i] == ' ' || (str[i] >= 9 && str[i] <= 13))
	{
		i++;
	}
	sign = 1;
	while (str[i] == '+' || str[i] == '-')
	{
		if (str[i] == '-')
		{
			sign = -sign;
		}
		i++;
	}
	nb = 0;
	while (str[i] >= '0' && str[i] <= '9')
	{
		nb = nb * 10 + (str[i] - '0');
		i++;
	}
	return (sign * nb);
}
int	main()
{
	printf("Teste 1: %d\n", ft_atoi("----++----132546e665"));
	printf("Teste 1: %d\n", ft_atoi("----++---132546e665"));
	printf("Teste 1: %d\n", ft_atoi("   --++--+--659a665"));
	return(0);
}